package bk2_02VariablesAndDataTypes;

public class UsingFinalVariables_Contants {
	//final/constant variable's value is immutable after initialization
	// final/constant variables can be class or instance variables
	final int WEEKDAYS = 7;
	final int workingDays = 5;
	final int weekEndDays = 2;
	
	//final class variable, add static final(not final static) to the variable 
	static final int MONTHS_OF_YEAR = 12;
	static final int BALL_RADIUS = 6;
}
